public class Strings {
    public static void main(String[] args) {
        int x = 5;
        String str = "Hello";
        String space = " ";
        String name = "Bob";
        System.out.println(str + space + name);
        System.out.println("Hello" + " " + "John");
        System.out.println("My age is " + x);
    }
}
